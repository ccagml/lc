// @lc code=start
class Solution
{
public:
    int lengthOfLongestSubstring(string s)
    {
        unordered_map<char, int> vi;
        int left = 0;
        int result = 0;
        for (int i = 0; i < s.size(); i++)
        {
            vi[s[i] - 'a']++;
            while (vi[s[i] - 'a'] > 1)
            {
                vi[s[left] - 'a']--;
                left++;
            }
            result = max(result, i - left + 1);
        }
        return result;
    }
};
